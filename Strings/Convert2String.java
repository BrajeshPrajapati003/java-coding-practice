public class Convert2String {
    public static void main(String[] args) {
        int i = 43;
        float f = 43.66f;
        long l = 436667;
        char ch = 'B';
        double d = 43.6667;
        boolean b = false;

        System.out.println("String value of i " + String.valueOf(i));
        System.out.println("String value of f " + String.valueOf(f));
        System.out.println("String value of l " + String.valueOf(l));
        System.out.println("String value of ch " + String.valueOf(ch));
        System.out.println("String value of d " + String.valueOf(d));
        System.out.println("String value of b " + String.valueOf(b));

    }
}